/**
 * Build a Map<surahAr, Map<ayahNumber, verseText>> from the supplement.
 * This is merged with the verse maps built from JUZ_AMMA_BANK entries.
 */
export function getSupplementVerseMaps(): Map<string, Map<number, string>> {
  const result = new Map<string, Map<number, string>>();
  for (const [surah, ayahs] of Object.entries(data)) {
    const inner = new Map<number, string>();
    for (const [n, text] of Object.entries(ayahs)) {
      inner.set(Number(n), text);
    }
    result.set(surah, inner);
  }
  return result;
}
